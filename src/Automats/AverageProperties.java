    public AverageProperties(Automaton automaton, java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        if (automaton != null) {
            sDepth.setValue(automaton.getDepth());
            tfRadius.setText(Double.toString(automaton.getRadius()));
            switch (automaton.getAverageType()) {
                case nearestNeighbors:
                    rbNearestNeighbors.setSelected(true);
                    break;
                case circle:
                    rbCircle.setSelected(true);
                    break;
            }
        }
        setLocationRelativeTo(null);
    }

