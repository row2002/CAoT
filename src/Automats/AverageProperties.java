    private void bOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOkActionPerformed
        clickedOK = true;

        automaton.setDepth((Integer) sDepth.getValue());
        automaton.setRadius(Double.parseDouble(tfRadius.getText()));
        if (rbNearestNeighbors.isSelected()) automaton.setAverageType(AverageType.nearestNeighbors);
        else
        if (rbCircle.isSelected()) automaton.setAverageType(AverageType.circle);
        else
        automaton.setAverageType(AverageType.none);
        
        dispose();
    }//GEN-LAST:event_bOkActionPerformed

